package br.com.sigauti.auxiliar.entity;

public class TipoStatus {
	
	private Integer IDTipoStatus;
	private String DescTipoStatus;
	
	
	public Integer getIDTipoStatus() {
		return IDTipoStatus;
	}
	public void setIDTipoStatus(Integer iDTipoStatus) {
		IDTipoStatus = iDTipoStatus;
	}
	public String getDescTipoStatus() {
		return DescTipoStatus;
	}
	public void setDescTipoStatus(String descTipoStatus) {
		DescTipoStatus = descTipoStatus;
	}

}
